#include <iostream>
#include "addition.hpp"

/**
 * @brief Adds two number 
 * @param a first integer
 * @param b second integer
 * @return int : a + b
 * @deprecated use sum insted 
 */
int add(int a, int b) {
    return a + b;
}

/**
 * @brief Adds two number 
 * @param a first integer
 * @param b second integer
 * @return long : a + b
 */
long sum(long a, long b) {
    return a + b;
}